#include <unistd.h>

void	printer(int a, int b)
{
	char	c_a[2];
	char	c_b[2];

	c_a[0] = '0' + a / 10;
	c_a[1] = '0' + a % 10;
	c_b[0] = '0' + b / 10;
	c_b[1] = '0' + b % 10;
	write(1, &c_a, 2);
	write(1, " ", 1);
	write(1, &c_b, 2);
	if (a != 98 || b != 99)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = -1;
	b = -1;
	while (a++ < 99)
	{
		while (b++ < 99)
		{
			if (a < b)
			{
				printer(a, b);
			}
		}
		b = 0;
	}
}

void	ft_truc_a_un(int **tab)
{
	int	x;
	int	y;

	x = 1;
	y = 1;
	while (x <= 4)
	{
		while (y <= 4)
		{
			tab[x][y] = 1;
			y++;
		}
		x++;
		y = 1;
	}
}
